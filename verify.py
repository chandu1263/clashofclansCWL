class Clan_tag_verifier:
    """ 
        Verifies the given clan tag
    """
    
    def clan_tag_verifier(self, clan_tag):
        """[summary]

        Args:
            clan_tag ([String]): [Clan Tag]

        Returns:
            [Boolean]: [whether a valid clan tag]
        """
        if clan_tag[0] != '#':
            return False
        if len(clan_tag) <= 7 or len(clan_tag) >= 10:
            return False
        return True
            